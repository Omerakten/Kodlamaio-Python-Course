# SELENİUM ACTİONS LİST
# click	                        :Bir öğeyi tıklar.
# click_and_hold	            :Bir öğe üzerinde farenin sol düğmesini basılı tutar.
# context_click	                :Bir öğe üzerinde bağlam tıklaması (sağ tıklama) gerçekleştirir.
# double_click	                :Bir öğeyi çift tıklar.
# drag_and_drop	                :Kaynak öğe üzerinde farenin sol düğmesini basılı tutar, ardından hedef öğeye gider ve fare düğmesini bırakır.
# drag_and_drop_by_offset	    :Kaynak eleman üzerinde sol fare düğmesini basılı tutar, ardından hedef ofsete hareket eder ve fare düğmesini bırakır.
# key_down	                    :Serbest bırakmadan yalnızca bir tuşa basma gönderir.
# key_up	                    :Bir değiştirici tuşu serbest bırakır.
# move_by_offset	            :Fareyi, geçerli fare konumundan bir uzaklığa taşıma.
# move_to_element	            :Fareyi bir öğenin ortasına hareket ettirmek.
# move_to_element_with_offset	:Fareyi belirtilen öğenin bir ötelemesi kadar hareket ettirin, Ötelemeler, öğenin sol üst köşesine göredir.
# perform	                    :Depolanan tüm eylemleri gerçekleştirir.
# pause	                        :Tüm girişleri saniye cinsinden belirtilen süre boyunca duraklat
# release	                    :Bir öğe üzerinde tutulan bir fare düğmesini serbest bırakma.
# reset_actions	                :Halihazırda yerel olarak ve uzak uçta depolanan eylemleri temizler
# send_keys	                    :Anahtarları geçerli odaklanılan öğeye gönderir.


